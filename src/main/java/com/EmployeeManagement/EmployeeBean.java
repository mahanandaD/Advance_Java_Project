package com.EmployeeManagement;

public class EmployeeBean {
	private String eid;
	private String ename;
	private String edesg;
	private long bsal;
	private long hra;
	private long da;
	private long tatsal;
	public EmployeeBean() {
		
	}
	public String getEid() {
		return eid;
	}
	public void setEid(String eid) {
		this.eid = eid;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public String getEdesg() {
		return edesg;
	}
	public void setEdesg(String edesg) {
		this.edesg = edesg;
	}
	public long getBsal() {
		return bsal;
	}
	public void setBsal(long bsal) {
		this.bsal = bsal;
	}
	public long getHra() {
		return hra;
	}
	public void setHra(long hra) {
		this.hra = hra;
	}
	public long getDa() {
		return da;
	}
	public void setDa(long da) {
		this.da = da;
	}
	public long getTatsal() {
		return tatsal;
	}
	public void setTatsal(long tatsal) {
		this.tatsal = tatsal;
	}
	
	
}
	